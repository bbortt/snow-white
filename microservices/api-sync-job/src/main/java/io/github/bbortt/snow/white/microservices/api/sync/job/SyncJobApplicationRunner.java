@Component
@Profile("!test")
@RequiredArgsConstructor
public class SyncJobApplicationRunner implements ApplicationRunner {

  private final SyncJob syncJob;

  @Override
  public void run(ApplicationArguments args) {
    syncJob.syncCatalog();
  }
}
